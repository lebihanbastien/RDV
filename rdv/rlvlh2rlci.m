function [zrlci] = rlvlh2rlci(t, zlvlh, zlci_t, mu)
% Change of coordinates: from LVLH to RLCI coordinates.

%--------------------------------------------------------------------------
% Rotation matrix
%--------------------------------------------------------------------------
Cs = lvlh2lciRotMat(zlci_t, t, mu);

%--------------------------------------------------------------------------
% COC
%--------------------------------------------------------------------------
zrlci = Cs*zlvlh;

end
