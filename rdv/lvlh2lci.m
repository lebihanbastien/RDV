function [zlci] = lvlh2lci(t, zlvlh, zlci_t, mu)
% Change of coordinates: from LVLH to LCI coordinates.

%--------------------------------------------------------------------------
% Rotation matrix
%--------------------------------------------------------------------------
Cs = lvlh2lciRotMat(zlci_t, t, mu);

%--------------------------------------------------------------------------
% COC
%--------------------------------------------------------------------------
zlci = Cs*zlvlh + zlci_t;

end
